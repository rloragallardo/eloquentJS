// ! Una forma de agrupar valores, recolectarlos es un Array, un array es una estructura de datos que se define como una lista de valores encerrados entre corchetes, y separados por una coma 
// * La notacion para acceder a los valores tambien usa corchetes

let listaDeNumeros = [1,2,3,4,5,6];
console.log(listaDeNumeros[0]); // 1
console.log(listaDeNumeros[1]); // 2
console.log(listaDeNumeros[2]); // 3
console.log(listaDeNumeros[3]); // 4
console.log(listaDeNumeros[4]); // 5
console.log(listaDeNumeros[5]); // 6

// ! tener en cuenta que el numero encerrado entre corchetes a la hora de acceder a los valores dentro del array se llama indice, que nos indica la posicion del elemento al que estamos accediendo, un array tiene la caracteristica de que el primer elemento se encuentra en la posicion CERO 
